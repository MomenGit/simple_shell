/**
 * _strcmp - compares two strings
 * @s1: first string of the comparison
 * @s2: second string of the comparison
 *
 * Return:
 * 0, if equal
 * -ve number, if s1 smaller than s2
 * +ve number, if s2 is greater than s2
 * On error, .
 */
int _strcmp(char *s1, char *s2)
{
	int res, i;

	res = 0;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
		{
			res = s1[i] - s2[i];
			break;
		}
		i++;
	}

	return (res);
}

/**
 * _atoi - converts the  string to an integer
 * @s: string to be converted
 * Return:  if no numbers in string return 0,  otherwise converted number
 */
int _atoi(char *s)
{
	int i, sign = 1, flag = 0, output;
	unsigned int result = 0;

	for (i = 0; s[i] != '\0' && flag != 2; i++)
	{
		if (s[i] == '-')
			sign *= -1;

		if (s[i] >= '0' && s[i] <= '9')
		{
			flag = 1;
			result *= 10;
			result += (s[i] - '0');
		}
		else if (flag == 1)
			flag = 2;
	}

	if (sign == -1)
		output = -result;
	else
		output = result;

	return (output);
}
